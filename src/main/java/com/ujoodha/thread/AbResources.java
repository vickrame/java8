package com.ujoodha.thread;

public abstract class AbResources implements IResource {

	protected int solde;

	/**
	 * @return the solde
	 */
	public int getSolde() {
		return solde;
	}

	/**
	 * @param solde
	 *            the solde to set
	 */
	public void setSolde(int solde) {
		this.solde = solde;
	}

}
